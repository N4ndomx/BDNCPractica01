    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Producto p = new Producto();
        p.setDescripcion(txtDesc.getText());
        p.setPrecioVenta(Double.parseDouble(txtPVenta.getText()));
        p.setPrecioCompra(Double.parseDouble(txtPCompra.getText()));

        boolean res = daoP.guardar(p);
        if (res) {
            JOptionPane.showMessageDialog(rootPane, "Producto Guardado");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error");

        }

    }//GEN-LAST:event_btnGuardarActionPerformed

