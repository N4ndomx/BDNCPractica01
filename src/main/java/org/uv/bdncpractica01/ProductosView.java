    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Producto p = new Producto();

        p.setId(Integer.parseInt(txtClave.getText()));
        p = daoP.buscarById(p.getId());
        if (p != null) {
            txtDesc.setText(p.getDescripcion());
            txtPCompra.setText(String.valueOf(p.getPrecioCompra()));
            txtPVenta.setText(String.valueOf(p.getPrecioVenta()));

        } else {
            JOptionPane.showMessageDialog(rootPane, "Producto no encontrado");

        }

    }//GEN-LAST:event_jButton2ActionPerformed

